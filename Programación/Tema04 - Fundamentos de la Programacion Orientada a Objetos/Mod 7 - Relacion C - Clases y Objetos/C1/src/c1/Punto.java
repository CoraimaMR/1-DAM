public class Punto {
    //----------- ATRIBUTOS -----------
    private int x;
    private int y;
    
    //--------- CONSTRUCTURES ---------
    public Punto (int x){
        this.x = x;
    }  // Constructor
    
    public Punto(int x, int y){
        this.x = x;
        this.y = y;
    }  // Constructor
    
    //------- GETTERS Y SETTERS -------
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    //------ METODOS DE LA CLASE ------
    
    } // clase principal

